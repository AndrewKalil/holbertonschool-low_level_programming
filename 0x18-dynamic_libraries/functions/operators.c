#include "operators.h"
#include <stdio.h>

/**
 * add - returns the sum of two numbers
 * @a: number 1
 * @b: number 2
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the differences between two numbers
 * @a: number 1
 * @b: number 2
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the product of two numbers
 * @a: number 1
 * @b: number 2
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns the quotient of two numbers
 * @a: number 1
 * @b: number 2
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the remainder of dividing two numbers
 * @a: number 1
 * @b: number 2
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
